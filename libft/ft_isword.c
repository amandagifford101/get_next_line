#include "libft.h"

int	ft_isword(char *str)
{
	int i;

	i = 0;
	while (str)
	{
		if (str[i] != ' ' &&
			str[i] != '\n' &&
			str[i] != '\t' &&
			str[i] != '\v' &&
			str[i] != '\f' &&
			str[i] != '\r')
			return (1);
		i++;
	}
	return (0);
}
